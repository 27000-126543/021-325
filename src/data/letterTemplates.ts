const LETTER_TITLES: Record<LetterType, string> = {
  intent_notice: '索赔意向通知书',
  claim_report: '费用索赔报告',
  reminder: '索赔催办函',
};

export function getLetterTitle(type: LetterType): string {
  return LETTER_TITLES[type];
}

export function getTemplate(claimType: ClaimType): LetterSectionTemplates {
  return TEMPLATES[claimType];
}

function getToneVariant(variant: ToneVariant, toneLevel: number): string {
  if (toneLevel <= 33) return variant.soft;
  if (toneLevel <= 66) return variant.medium;
  return variant.hard;
}

export function pickToneText(variant: ToneVariant, toneLevel: number): string {
  return getToneVariant(variant, toneLevel);
}

